
export class User {

    constructor(id:string,
                firstName:string,
                lastName:string,
                emailAddress:string,
                password:string,
                token:string) {

        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.emailAddress = emailAddress;
        this.password = password;
        this.token = token;
    }

}

